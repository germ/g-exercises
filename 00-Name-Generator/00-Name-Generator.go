func genName() string {
	first, last := rand.Intn(len(fNames)), rand.Intn(len(lNames))
	return fmt.Sprintf("%v %v", fNames[first], lNames[last])
}

// Return three common words, a symbol and number
func genPassword() (pw string) {
	for i := 0; i < 3; i++ {
		// Mix case 33 percent of the time
		wordIdx := rand.Intn(len(words))
		if rand.Int()%3 == 0 {
			pw += strings.ToUpper(words[wordIdx])
		} else {
			pw += words[wordIdx]
		}
	}
	pw += string(specials[rand.Intn(len(specials))])
	pw += string(numbers[rand.Intn(len(numbers))])

	return
}

